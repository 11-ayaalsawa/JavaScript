"use strict";



function myflag() {
    
let sss= document.getElementById("sel").value;
    

if (sss=="Jordan"){
    
    document.getElementById("image").src="https://flagsworld.org/img/cflags/jordan-flag.png";
   
}

else if (sss == " Palestine") {
    document.getElementById("image").src="https://upload.wikimedia.org/wikipedia/commons/thumb/f/f4/Palestine_Flag.svg/1200px-Palestine_Flag.svg.png";

}



else if (sss == "Turkey") {
    document.getElementById("image").src="https://upload.wikimedia.org/wikipedia/commons/thumb/b/b4/Flag_of_Turkey.svg/800px-Flag_of_Turkey.svg.png";

}

else if (sss == "France") {
    document.getElementById("image").src="https://www.osculati.com/SupplyImages/WF-0058/EN_Bandiera_francia_B.jpg";

}

}

